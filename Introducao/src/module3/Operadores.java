package module3;

public class Operadores {
    public static void main(String[] args) {
        // = -> Atribuição
        // == -> Comparação
        // != -> Diferente
        // > -> Maior
        // < -> Menor
        // >= -> Maior ou igual
        // <= -> Menor ou igual

        int a = 9;
        System.out.println(++ a);

        String result = (a > 10) ? "Maior" : "Menor";
        System.out.println(result);
    }
}
