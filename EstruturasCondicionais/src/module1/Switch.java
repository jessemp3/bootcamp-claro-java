package module1;

public class Switch {
    public static void main(String[] args) {
        String casei = "2";

        switch (casei) {
            case "1":
                System.out.println("Case 1");
                break;
            case "2":
                System.out.println("Case 2");
                break;
            case "3":
                System.out.println("Case 3");
                break;
            default:
                System.out.println("Default case");
        }
    }
}
